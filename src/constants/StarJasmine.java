package constants;

public class StarJasmine extends Plant {
    public StarJasmine(String name, String category, String family, String price, String image, String description) {
        super(name, category, family, price, image, description);
    }

    // Getter for the name property
    public String getName() {
        return super.getName();
    }

    // Setter for the name property
    public void setName(String name) {
        super.setName(name);
    }

    // Getter for the category property
    public String getCategory() {
        return super.getCategory();
    }

    // Setter for the category property
    public void setCategory(String category) {
        super.setCategory(category);
    }

    // Getter for the family property
    public String getFamily() {
        return super.getFamily();
    }

    // Setter for the family property
    public void setFamily(String family) {
        super.setFamily(family);
    }

    // Getter for the price property
    public String getPrice() {
        return super.getPrice();
    }

    // Setter for the price property
    public void setPrice(String price) {
        super.setPrice(price);
    }

    // Getter for the image property
    public String getImage() {
        return super.getImage();
    }

    // Setter for the image property
    public void setImage(String image) {
        super.setImage(image);
    }

    // Getter for the description property
    public String getDescription() {
        return super.getDescription();
    }

    // Setter for the description property
    public void setDescription(String description) {
        super.setDescription(description);
    }
}
